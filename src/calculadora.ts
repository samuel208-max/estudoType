let numero1: number = 2;
let numero2: number = 5;

function somar() {
    return `A soma dos dois numeros é ${numero1 + numero2}`;
}

function multiplicar() {
    return numero1 * numero2;
}

function subtração() {
    return numero1 - numero2;
}

function divisão() {
    return numero1 / numero2;
}

somar();
multiplicar();
subtração();
divisão();